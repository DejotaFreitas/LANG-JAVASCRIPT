"use strict";
//=======================================================
// executa depois de um tempo
let timeout = setTimeout(()=>{ console.log("clearTimeout"); }, 1000);
// cancela o timeout
setTimeout(()=>{ clearTimeout(timeout); }, 100);

//=======================================================
// executa a cada terminado tempo em lop infinito
let interval = setInterval(()=>{ console.log(new Date()); }, 1000);
// cancela setInterval
setTimeout(()=>{ clearInterval(interval); }, 10000);
