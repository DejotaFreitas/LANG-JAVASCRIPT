function popular_cidade(array) {
  for (var key in array) {
    var option = document.createElement("option");
    option.appendChild(document.createTextNode(array[key]));
    if (cidade.getAttribute("value") == array[key]) { option.setAttribute('selected',''); }
    cidade.appendChild(option);
  }
}

function popular_estado() {
  var option = document.createElement("option");
  option.value = '';
  option.appendChild(document.createTextNode('Selecione o estado'));
  estado.appendChild(option);
  for (var key in estados) {
    var option = document.createElement("option");
    option.value = key;
    option.appendChild(document.createTextNode(estados[key]));
    if (estado.getAttribute("value") == key) { option.setAttribute('selected',''); }
    estado.appendChild(option);
  }
}


